package com.zakoraio.reviews.controller.view.data;

public class UserCourseReview {
	private String comment;
	private String noOfProjects;
	private String noOfQuizes;
	private String noOfExams;
	private String noOfAssignements;
	private String offeringid;
	public String getComment() {
		return comment;
	}
	public void setComment(String comment) {
		this.comment = comment;
	}
	public String getNoOfProjects() {
		return noOfProjects;
	}
	public void setNoOfProjects(String noOfProjects) {
		this.noOfProjects = noOfProjects;
	}
	public String getNoOfQuizes() {
		return noOfQuizes;
	}
	public void setNoOfQuizes(String noOfQuizes) {
		this.noOfQuizes = noOfQuizes;
	}
	public String getNoOfExams() {
		return noOfExams;
	}
	public void setNoOfExams(String noOfExams) {
		this.noOfExams = noOfExams;
	}
	public String getNoOfAssignements() {
		return noOfAssignements;
	}
	public void setNoOfAssignements(String noOfAssignements) {
		this.noOfAssignements = noOfAssignements;
	}
	public String getOfferingid() {
		return offeringid;
	}
	public void setOfferingid(String offeringid) {
		this.offeringid = offeringid;
	}
	
}